n = 0:100;
x = 1.01.^n;
N = length(x);

% α) πεδίο διακριτού χρόνου
E = sum(abs(x).^2);
disp("Ενέργεια στο πεδίο του χρόνου:");
disp(E);

% β) DFT  
X = fft(x);     % Υπολογισμός DFT 

% Υπολογισμός ενέργειας από DFT σύμφωνα με τo τύπο
% από το θεώρημα Parversal 
E =(1/N)*sum(abs(X).^2);
disp("Ενέργεια με DFT:");
disp(E);

