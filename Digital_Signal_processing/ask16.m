disp('Η συνάρτηση μεταφοράς με tf()');
H = tf(n, d, Ts)

pzmap(H);

% (β)
syms z X Y;
Y1 = (z^-1)*Y;
X1 = (z^-1)*X;
G = (Y+0.3*Y1)-(X+X1);
SOL = solve(G,Y);
H = SOL/X;
H = simplify(H);

disp('Η συνάρτηση μεταφοράς με solve()');
pretty(H);

