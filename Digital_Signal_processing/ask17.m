syms z X Y;
Y1 =(z^-1)*Y;
G = (Y-0.9*Y1)-(X);
SOL = solve(G,Y);
H = SOL/X;
H = simplify(H);

disp('Η συνάρτηση μεταφοράς');
pretty(H);

% Απο το τύπωμα βλέπω ότι ισχύουν οι ακόλουθοι συντελεστές της συνάρτησης
% μεταφοράς για το αριθμητή και παρανομαστή αντίστοιχα:
num = [1 1];
den = [1 -0.9];

y = dimpulse(num,den);       % κρουστική απόκριση
stairs(0:length(y) - 1, y);  % σχεδιασμός κρουστικής απόκρισης
legend('κρουστική απόκριση')

figure;
zplane(num,den) % σχεδιασμός πόλων μηδενικών