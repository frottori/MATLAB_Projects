% Ορισμός σημάτων
x1 = zeros(1,21); % αρχικοποιήση διανύσματος
x2 = zeros(1,21);
for i = 1:21
    x1(i) = randn();
    x2(i) = randn();
end

a = 4;
b = 5;

% Υπολογισμός της αριστερής σχέσης
A = fft(a*x1 + b*x2, 21);
disp("Αριστερή σχέση:");
disp(A);

% Υπολογισμός δεξιάς σχέσης
X1 = fft(x1, 21);
X2 = fft(x2, 21);
B = a*X1 + b*X2;
disp("Δεξιά σχέση:");
disp(B);